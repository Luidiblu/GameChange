def separate
  puts "\n" + "="*100 + "\n" + "\n"
end

def record_new(instance)
  if instance.save
    puts "---#{instance.name} was created!"
  else
    puts "=========================== Something went wrong =========================="
    puts "=================== #{instance.errors.messages} ==================="
  end
end

